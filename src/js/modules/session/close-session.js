import { redirectToLogin } from '../helpers/redirect';
//* ---------------------------------------------------------------------------------------------

//* ---------------------------------------------------------------------------------------------
//* FUNCIÓN (closeSession)
//* ---------------------------------------------------------------------------------------------
const closeSession = () => {
 sessionStorage.removeItem('session');
 redirectToLogin();
};

export default closeSession;
