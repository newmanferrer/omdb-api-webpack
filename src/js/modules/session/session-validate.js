//* FUNCIÓN QUE VALIDA CONSTANTEMENTE SI EXISTE UNA SESIÓN ACTIVA
//* ===============================================================================================
//* ---------------------------------------------------------------------------------------------
//* IMPORTACIONES
//* ---------------------------------------------------------------------------------------------
import { getSessionStorage } from '../storage/local-session-storage';
import { redirectToLogin } from '../helpers/redirect';
//* ---------------------------------------------------------------------------------------------

//* ---------------------------------------------------------------------------------------------
//* FUNCIÓN (sessionValidate)
//* ---------------------------------------------------------------------------------------------
const sessionValidate = () => {
 const sessionActive = getSessionStorage();
 const pageLocation = window.location.pathname;

 if ((pageLocation === '/search.html' || pageLocation === '/favorites.html') && !sessionActive) {
  redirectToLogin();
 }
};

export default sessionValidate;
